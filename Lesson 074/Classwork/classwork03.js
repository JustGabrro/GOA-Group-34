// Math.abs აბრუნებს რიცხვის აბსოლიტურ მნიშვნელობას
console.log(Math.abs(-10));

// Math.round აყალიბებს რიცხვს ყველაზე ახლო მთელ რიცხვამდე
console.log(Math.round(3.7));

// Math.ceil აყალიბებს რიცხვს ზემოთ მთლიან რიცხვამდე
console.log(Math.ceil(6.3));

// Math.floor აყალიბებს რიცხვს ქვემოთ მთლიან რიცხვამდე
console.log(Math.floor(3.6));

// Math.max აბრუნებს ყველაზე დიდ რიცხვს
console.log(Math.max(1, 5, 3));

// Math.min აბრუნებს ყველაზე პატარა რიცხვს
console.log(Math.min(1, 5, 3));

// Math.random აბრუნებ შემთხვევით რიცხვს 0დან 1მდე
console.log(Math.random());

// Math.sqrt აბრუნებს რიცხვის კვადრატულ ფესვს
console.log(Math.sqrt(17));

// Math.pow აბრუნებს რიცხვის სიმრავლეს
console.log(Math.pow(2, 3));