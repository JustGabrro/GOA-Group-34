// Creating an object
let person = {
    name: "Gabriel",
    age: 17,
    city: "Tbilisi"
};

// Dot notation is used to access object properties
console.log(person.name); // Outputs "Gabriel"

// Dot notation can be used to change the value of an object property
person.age = 18; // Changes the value of age to 18

// Dot notation can be used to add new properties to an object
person.job = "Programmer"; // Adds a new property "job"

// Dot notation is used to call methods
let str = "goa";
console.log(str.toUpperCase()); // Outputs "GOA"

// Dot notation is used to access properties of built-in objects
console.log(Math.PI); // Outputs the value of P

// Dot notation can be chained
let sentence = "   GOA ACADEMY          ";
console.log(sentence.trim().toLowerCase()); // Outputs "goa academy"

