user_week = int(input("Enter a day: "))

if user_week == 1:
    print("1 is Monday")
elif user_week == 2:
    print("2 is Tuesday")
elif user_week == 3:
    print("3 is Wednesday")
elif user_week == 4:
    print("4 is Thursday")
elif user_week == 5:
    print("5 is Friday")
elif user_week == 6:
    print("6 is Saturday")
elif user_week == 7:
    print("7 is Sunday")
else:
    print("Sorry, I don't know that much") 